namespace Panlingo.LanguageIdentification.Whatlang
{
    /// <summary>
    /// Language scripts due to ISO 15924.
    /// 
    /// https://en.wikipedia.org/wiki/ISO_15924
    /// </summary>
    public enum WhatLangScript : byte
    {
        Arab = 0, // Arabic
        Beng = 1, // Bengali
        Cyrl = 2, // Cyrillic
        Deva = 3, // Devanagari
        Ethi = 4, // Ethiopic
        Geor = 5, // Georgian
        Grek = 6, // Greek
        Gujr = 7, // Gujarati
        Guru = 8, // Gurmukhi
        Hang = 9, // Hangul
        Hebr = 10, // Hebrew
        Hira = 11, // Hiragana
        Knda = 12, // Kannada
        Kana = 13, // Katakana
        Khmr = 14, // Khmer
        Latn = 15, // Latin
        Mlym = 16, // Malayalam
        Mand = 17, // Mandarin
        Mymr = 18, // Myanmar
        Orya = 19, // Oriya (now officially Odia)
        Sinh = 20, // Sinhala
        Taml = 21, // Tamil
        Telu = 22, // Telugu
        Thai = 23  // Thai
    }
}

